FindHeartRates.find = function(origage, units, centile) {
    if (origage=="") {alert('no age'); return undefined; }
    if (origage==undefined) { alert('no age'); return undefined; }
    if (centile==undefined) { centile = "median"; }
    // convert age to months
    switch (units) {
        case "years":
            age = origage*12;
            break;
        case "weeks":
            age = origage / 52 * 12;
            break;
        case "days":
            age = origage / 365 * 12;
            break;
        case "months":
            age = origage*1;
            break;
        default: 
            return undefined;
    }
    // is age outside 0-18 years (0-216 months)?
    if (age<0 || age>216) {
        return undefined;
    }
    // is it a round number of months?
    if (Math.round(age)===age) {
        return LookupHeartRates[centile](age);
    }
    else if (age<2){ // under 2 months needs finer interpolation
        // calculate age in days
        switch (units) {
               case "years":
                   ageindays = origage*365;
                   break;
               case "weeks":
                   ageindays = origage * 7;
                   break;
               case "days":
                   ageindays = origage * 1;
                   break;
               case "months":
                   ageindays = origage * 12 / 365;
                   break;
               default: 
                   return undefined;
        }
        // data is stored in 2-day intervals...
        ageabove = Math.ceil(ageindays/2)*2;
        agebelow = Math.floor(ageindays/2)*2;
        hrabove = LookupInfantHeartRates[centile](ageabove);
        if (ageabove==agebelow) { // don't need to interpolate further
            return hrabove;
        }
        hrbelow = LookupInfantHeartRates[centile](agebelow);
        hrhere = hrbelow + ((hrabove-hrbelow) * (age-agebelow) / (ageabove-agebelow));
        return hrhere;
    }
    else {    // interpolate
        ageabove = Math.ceil(age);
        agebelow = Math.floor(age);
        hrabove = LookupHeartRates[centile](ageabove);
        hrbelow = LookupHeartRates[centile](agebelow);
        hrhere = hrbelow + ((hrabove-hrbelow) * (age-agebelow) / (ageabove-agebelow));
        return hrhere;
    }
}

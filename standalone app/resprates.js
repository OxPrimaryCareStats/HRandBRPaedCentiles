FindRespRates.find = function(age, units, centile) {
    if (age==undefined) { return undefined; }
    if (centile==undefined) { centile = "median"; }
    // convert age to months
    switch (units) {
        case "years":
            age = age*12;
            break;
        case "weeks":
            age = age / 52 * 12;
            break;
        case "days":
            age = age / 365 * 12;
            break;
        case "months":
            age = age*1;
            break;
        default: 
            return undefined;
    }
    // is age outside 0-18 years (0-216 months)?
    if (age<0 || age>216) {
        return undefined;
    }
    // is it a round number of months?
    if (Math.round(age)===age) {
        return LookupRespRates[centile](age);
    }
    else {
        // interpolate
        ageabove = Math.ceil(age);
        agebelow = Math.floor(age);
        rrabove = LookupRespRates[centile](ageabove);
        rrbelow = LookupRespRates[centile](agebelow);
        rrhere = rrbelow + ((rrabove-rrbelow) * (age-agebelow) / (ageabove-agebelow));
        return rrhere;
    }
}
